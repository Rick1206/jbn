</li>
<li>
In addition to physical needs, this Home also cares for the mental health of each child, whose inner mind and soul need to be touched by love. Therefore, this Home emphasizes providing an atmosphere with family love, where each child will be cared according to their individual physical needs and personalities as well. This Home hopes each child will not only grow strongly both physical and mental, but also grows with strong characters that lead them to an abundant life.
</li>
<li>
