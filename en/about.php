China has entered into a fast economic growth stage. Many young parents have strong financial burdens and are very busy at work. They pay no attention to health knowledge during pregnancy and make wrong decisions when they see their babies with health problems. There are hundreds of thousands babies abandoned and sent to the government-run orphanages each year. As a blessing channel of many individuals, non-profit organizations, and corporates,  this Home will work with government-run orphanages to share their burdens and to give orphans a bright future.
</li>
